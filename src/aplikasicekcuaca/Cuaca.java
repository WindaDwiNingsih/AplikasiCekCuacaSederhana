    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String apiKey = "6c8b1f4b9ff5233a12fa627e30474013"; // Ganti dengan API key Anda
    String kota = textFieldKota.getText(); // Ambil input dari JTextField

    if (kota.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama kota terlebih dahulu!");
        return;
    }

    // URL API OpenWeatherMap
    String urlString = "https://api.openweathermap.org/data/2.5/weather?q=" + kota + "&appid=" + apiKey + "&units=metric";

    try {
        // Membuat koneksi ke API
        URL url = new URL(urlString);
        HttpURLConnection connection = (HttpURLConnection) url.openConnection();
        connection.setRequestMethod("GET");

        // Membaca respons dari API
        BufferedReader reader = new BufferedReader(new InputStreamReader(connection.getInputStream()));
        StringBuilder response = new StringBuilder();
        String line;

        while ((line = reader.readLine()) != null) {
            response.append(line);
        }
        reader.close();

        // Parsing respons JSON secara manual
        String jsonResponse = response.toString();
        String weatherDescription = jsonResponse.split("\"description\":\"")[1].split("\"")[0];
        String temperature = jsonResponse.split("\"temp\":")[1].split(",")[0];

        
        modelTabel.addRow(new Object[]{kota, weatherDescription, temperature});
        
        
        // Simpan kota ke daftar favorit
        if (!daftarFavorit.contains(kota)) {
            daftarFavorit.add(kota); // Tambahkan kota ke set
            comboBoxModel.addElement(kota); // Tambahkan ke JComboBox
        }
        
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengambil data.");
    }
}        // TODO add your handling code here:

        

